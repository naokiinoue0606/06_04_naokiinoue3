package Function.colLesson03;

public class Word3 {

        String word;
        String meaning;

        public Word3() {
        }

        public Word3(String word, String meaning) {
            this.meaning = meaning;
            this.word = word;
        }

        public String toString() {
            return "たんごは" + this.word + "意味は" + this.meaning;

        }
    }
