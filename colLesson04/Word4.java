package Function.colLesson04;

public class Word4 {
    String word;
    String meaning;

    public Word4(){}

    public Word4(String word, String meaning) {
        this.meaning = meaning;
        this.word = word;
    }

    public String toString() {
        return "たんごは" + this.word + "意味は" + this.meaning;

    }

}
